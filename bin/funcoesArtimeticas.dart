void funcoesAritmeticas(List<String> arguments) {
  //funções artiméticas
  int num1 = 53;
  int num2 = 41;
  dynamic result = 0;

  //adição
  result = num1 + num2;
  //subtração
  result = num1 - num2;
  //multiplicação
  result = num1 * num2;
  //divisão
  result = num1 / num2;
  //divisão retornando um numero inteiro
  result = num1 ~/ num2;
  //resto da divisão
  result = num1 % num2;
  //incrementando
  num1++;
  //decrementando
  num1--;
}
